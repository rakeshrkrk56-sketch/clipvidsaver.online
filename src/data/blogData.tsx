export const getBlogPostBySlug = (slug: string): BlogPostFull | undefined => {
  const meta = BLOG_POSTS_META.find(post => post.slug === slug);
  if (!meta) return undefined;
  
  return {
    ...meta,
    content: BLOG_POSTS_CONTENT[slug]
  };
};

